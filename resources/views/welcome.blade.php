         <h3>Objectives</h3> <br>

          There are eight objectives of the university:<br>

          <br>
          (a) To share the genuine Theravāda Buddhism (the teachings of Gotama Buddha) with the people of the world.<br>
          (b) To study and comprehend the canonical texts of Theravāda Pitaka as approved successively by the six Buddhist Councils.<br>
          (c) To abstain from evil deeds and practice good deeds.<br>
          (d) To promote the four modes of sublime living (Brahma-vihāra) which would lead to the establishment of peaceful and prosperous world.<br>
          (e) To train more missionaries endowed with good morality who are well-versed in Pitaka literature and meditational practices.<br>
          (f) To teach and train local and foreign students who are willing to study Theravāda Pitaka in its pristine form.<br>
          (g) To collaborate with local and foreign universities, and also to cooperate with other universities that will support the objectives of the university.<br>    (h) To send missionaries to foreign countries for the propagation of Theravāda Buddhism.
